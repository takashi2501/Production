function f = model_ex1(t,IN)
f = zeros(5,1);

%パラメータ設定
ka1 = 10.0;
ka2 = 12.0;
kd1 = 1.0;
kd2 = 2.0;

%わかりやすくするためモデルのタンパク質名に置き換える
x1 = IN(1);
x2 = IN(2);
x3 = IN(3);
x4 = IN(4);
x5 = IN(5);

%反応速度式を記述
v1 = ka1 * x1 * x2 - kd1 * x3;
v2 = ka2 * x3 * x4 - kd2 * x5;

%微分方程式の右辺を記述
f(1) = -v1;
f(2) = -v1;
f(3) = +v1 - v2;
f(4) = -v2;
f(5) = +v2
